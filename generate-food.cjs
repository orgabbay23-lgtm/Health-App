const fs = require('fs');

const items = new Set();

const add = (str) => {
  if (!str) return;
  const cleaned = str.replace(/[0-9]/g, '').trim().replace(/\s+/g, ' ');
  if (cleaned.length > 1) {
    items.add(cleaned);
  }
};

const addCombos = (bases, mods, template) => {
  bases.forEach(b => {
    mods.forEach(m => {
      add(template.replace('%b', b).replace('%m', m));
    });
  });
};

const addTripleCombos = (list1, list2, list3, template) => {
  list1.forEach(i1 => {
    list2.forEach(i2 => {
      list3.forEach(i3 => {
        add(template.replace('%1', i1).replace('%2', i2).replace('%3', i3));
      });
    });
  });
};

